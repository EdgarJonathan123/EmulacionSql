    private String openFile(File archivo) {

        String documento = "";

        try {
            entrada = new FileInputStream(archivo);
            int ascii;
            while ((ascii = entrada.read()) != -1) {

                char caracter = (char) ascii;
                documento += caracter;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        return documento;

    }

    private String saveFile(File archivo, String documento) {

        String mensaje = null;

        try {
            salida = new FileOutputStream(archivo);
            byte[] bytxt = documento.getBytes();
            salida.write(bytxt);
            mensaje = "Archivo Guardado";

        } catch (Exception e) {
            e.printStackTrace();
        }

        return mensaje;
    }

    private void addPestana(String name, String contenido) {

        JScrollPane mipanel = new JScrollPane();
        JTextArea texto = new JTextArea();
        texto.setText(contenido);
        texto.setBackground(Color.CYAN);
        mipanel.setViewportView(texto);
        Pestanas.add(name, mipanel);
        pest++;

    }

