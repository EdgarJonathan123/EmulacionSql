    private void AbrirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirArchivoActionPerformed

        if (seleccionar.showDialog(null, "Abrir") == JFileChooser.APPROVE_OPTION) {
            archivo = seleccionar.getSelectedFile();

            if (archivo.canRead()) {

                if (archivo.getName().endsWith("rep")) {
                    String document = openFile(archivo);
                    addPestana(archivo.getName(), document);
                } else {
                    JOptionPane.showMessageDialog(null, "Archivo No Compatible");
                }

            }

        }

    }//GEN-LAST:event_AbrirArchivoActionPerformed

    private void GuardarComoArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarComoArchivoActionPerformed
        // TODO add your handling code here:

        if (seleccionar.showDialog(null, "Guardar") == JFileChooser.APPROVE_OPTION) {
            archivo = seleccionar.getSelectedFile();

            if (archivo.getName().endsWith("rep")) {
                //hay que  obtner el texto de la pestana seleccionada
                String Documento = "";
                String mensaje = saveFile(archivo, Documento);

                if (mensaje != null) {
                    JOptionPane.showMessageDialog(null, mensaje);
                } else {
                    JOptionPane.showMessageDialog(null, "Archivo no Compatible");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Guardar Documento de texto");
            }

        }
    }//GEN-LAST:event_GuardarComoArchivoActionPerformed

